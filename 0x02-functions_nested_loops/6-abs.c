/**
 * _abs - computes the absolute value of an integer
 *
 * Description: computes the absolute value of an integer
 *
 * @f: f is an integer.
 * Return: integer number
 */
int _abs(int f)
{
	if (f > 0)
	{
		return (f);
	}
	else if (f == 0)
	{
		return (f);
	}
	else
	{
		return (-f);
	}
}
