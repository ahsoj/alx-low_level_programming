#include "3-calc.h"
/**
 * op_add - calculate additions of @a and @b.
 * @a: integer
 * @b: integer
 */
int op_add(int a, int b)
{
	return (a+b);
}
/**
 * op_sub - calculate subtractions of @a and @b
 * @a: integer
 * @b: integer
 */
int op_sub(int a, int b)
{
	return (a-b);
}
/**
 * op_mul - calculate multiplacation operator of @a and @b.
 * @a: integer
 * @b: integer
 */
int op_mul(int a, int b)
{
	return (a*b);
}
/**
 * op_div - calculate division operation of @a and @b.
 * @: integer
 * @b: integer
 */
int op_div(int a, int b)
{
	return (a/b);
}
/**
 * op_mod - calculate remainder of division @ and @b.
 * @: integer
 * @b: integer
 */
int op_mod(int a, int b)
{
	return (a%b);
}
