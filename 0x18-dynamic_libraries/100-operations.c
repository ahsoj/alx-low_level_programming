
/*
 * add - sum the two inputs
 * @x: integer one
 * @y: integer two
 * 
 * Return: Sum of @x & @y
 */
int add(int x, int y){
	return x + y;
}

/*
 * sub - ssubtruct two integers
 * @x: integer one
 * @y: integer two
 *
 * Return: the result of @x & @y
 */
int sub(int x, int y){
	return x - y;
}

/*
 * mul - multiply two integers
 * @x: integer one
 * @y: integer two
 *
 * Return: the result of @x & @y
 */
int mul(int x, int y){
	return x * y;
}

/*
 * div - divide of @x over @y
 * @x: intger one
 * @y: integer two
 *
 * Return: the result of @x over @y
 */
int div(int x, int y){
	return x / y;
}

/*
 * mod - remainder of @x and @y
 * @x: integer one
 * @y: integer two
 *
 * Return: the remainder
 */
int mod(int x, int y){
	return x % y;
}

